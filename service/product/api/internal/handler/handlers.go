package handler

import (
	"net/http"
	"go-zero-ecommerce/service/product/api/internal/logic"
	"go-zero-ecommerce/service/product/api/internal/svc"
	"go-zero-ecommerce/service/product/api/internal/types"

	"github.com/zeromicro/go-zero/rest/httpx"
)

func ProductInfoHandler(svcCtx *svc.ServiceContext) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		var req types.ProductInfoReq
		if err := httpx.Parse(r, &req); err != nil {
			httpx.ErrorCtx(r.Context(), w, err)
			return
		}
		l := logic.NewProductInfoLogic(r.Context(), svcCtx)
		resp, err := l.ProductInfo(&req)
		if err != nil {
			httpx.ErrorCtx(r.Context(), w, err)
		} else {
			httpx.OkJsonCtx(r.Context(), w, resp)
		}
	}
}

